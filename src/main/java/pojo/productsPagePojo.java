package pojo;

public class productsPagePojo {

    private String products;
    private String productName;
    private String addToCartBtn;
    private String productAddedToaster;
    private String cartIcon;

    public String getProductAddedToaster() {
        return productAddedToaster;
    }

    public void setProductAddedToaster(String productAddedToaster) {
        this.productAddedToaster = productAddedToaster;
    }

    public String getCartIcon() {
        return cartIcon;
    }

    public void setCartIcon(String cartIcon) {
        this.cartIcon = cartIcon;
    }


    public String getProducts() {
        return products;
    }

    public void setProducts(String products) {
        this.products = products;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getAddToCartBtn() {
        return addToCartBtn;
    }

    public void setAddToCartBtn(String addToCartBtn) {
        this.addToCartBtn = addToCartBtn;
    }

}
