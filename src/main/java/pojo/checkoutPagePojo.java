package pojo;

public class checkoutPagePojo {
    private String selectCountry;
    private String countryName;
    private String placeOrderBtn;
    private String orderPageTitle;

    public String getSelectCountry() {
        return selectCountry;
    }

    public void setSelectCountry(String selectCountry) {
        this.selectCountry = selectCountry;
    }

    public String getOrderPageTitle() {
        return orderPageTitle;
    }

    public void setOrderPageTitle(String orderPageTitle) {
        this.orderPageTitle = orderPageTitle;
    }

    public String getPlaceOrderBtn() {
        return placeOrderBtn;
    }

    public void setPlaceOrderBtn(String placeOrderBtn) {
        this.placeOrderBtn = placeOrderBtn;
    }

    public String getCountryName() {
        return countryName;
    }

    public void setCountryName(String countryName) {
        this.countryName = countryName;
    }
}
